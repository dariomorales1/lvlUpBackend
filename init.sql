-- product_specifications.product_id -> products.id
ALTER TABLE product_specifications
    ADD CONSTRAINT fk_product_specifications_product
    FOREIGN KEY (product_id) REFERENCES products(id);

-- resenas.product_id -> products.id
ALTER TABLE resenas
    ADD CONSTRAINT fk_resenas_product
    FOREIGN KEY (product_id) REFERENCES products(id);

-- resenas.usuario_id -> usuarios.id
ALTER TABLE resenas
    ADD CONSTRAINT fk_resenas_usuario
    FOREIGN KEY (usuario_id) REFERENCES usuarios(id);

-- cart_items.cart_id -> carts.id
ALTER TABLE cart_items
    ADD CONSTRAINT fk_cart_items_cart
    FOREIGN KEY (cart_id) REFERENCES carts(id);

